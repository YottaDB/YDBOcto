#include <stdarg.h>
#include <stdlib.h>
#include <string.h>
#include <assert.h>

// Used to convert between network and host endian
#include <arpa/inet.h>

#include "message_formats.h"

NoData *make_no_data(void) {
	NoData *ret;

	ret = (NoData *)malloc(sizeof(NoData));
	memset(ret, 0, sizeof(NoData));

	ret->type = PSQL_NoData;
	ret->length = htonl(4);

	return ret;
}
